function shuffleArray(array) {
  const nextArray = [...array];

  for (let index = nextArray.length - 1; index > 0; index -= 1) {
    const swapIndex = Math.floor(Math.random() * (index + 1));
    [nextArray[index], nextArray[swapIndex]] = [nextArray[swapIndex], nextArray[index]];
  }

  return nextArray;
}

function generateAlphabetOptions(correct) {
  const options = new Set([correct]);
  const offsets = shuffleArray([-1, 1, -2, 2, -3, 3, -4, 4]);

  for (const offset of offsets) {
    if (options.size >= 4) break;
    const candidate = correct + offset;
    if (candidate >= 1 && candidate <= 26) {
      options.add(candidate);
    }
  }

  while (options.size < 4) {
    options.add(Math.floor(Math.random() * 26) + 1);
  }

  return shuffleArray([...options]);
}

function generateSquareOptions(correct, originalNum) {
  const options = new Set([correct]);
  const wrongAnswers = [
    (originalNum - 1) * (originalNum - 1),
    (originalNum + 1) * (originalNum + 1),
    correct + 10,
    correct - 10,
    correct + 100,
    correct - 100,
    originalNum * originalNum + originalNum,
    correct + Math.floor(Math.random() * 20),
    correct - Math.floor(Math.random() * 20),
  ].filter((value) => value > 0 && value !== correct);

  for (const wrongAnswer of shuffleArray(wrongAnswers)) {
    if (options.size >= 4) break;
    options.add(wrongAnswer);
  }

  while (options.size < 4) {
    let fallback = correct + Math.floor(Math.random() * 50) * (Math.random() > 0.5 ? 1 : -1);

    if (fallback <= 0) {
      fallback = correct + 10;
    }

    options.add(fallback);
  }

  return shuffleArray([...options]);
}

function generateCapitalOptions(correctCapital) {
  const options = new Set([correctCapital]);
  const capitals = shuffleArray(stateCapitals.map((item) => item.capital));

  for (const capital of capitals) {
    if (options.size >= 4) break;
    options.add(capital);
  }

  return shuffleArray([...options]);
}
